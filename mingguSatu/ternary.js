// Mendefinisikan variabel umur
let umur = 24;

// Menggunakan operator ternary untuk menentukan status berdasarkan umur
// Jika umur lebih besar atau sama dengan 18, maka status "Sudah Dewasa", jika tidak, status "Masih Anak-anak"
let status = (umur >= 18) ? "Sudah Dewasa" : "Masih Anak-anak";

// Menampilkan nilai
console.log(`Status: ${status}`);
